async function main() {
  console.log('Starting seed...')

  // 1. Create Default User
  const hashedPassword = await bcrypt.hash('securepassword', 10)
  const user = await prisma.user.upsert({
    where: { email: 'admin' },
    update: {},
    create: {
      email: 'admin',
      password_hash: hashedPassword,
      name: 'Администратор',
    },
  })
  console.log(`User created: ${user.email}`)

  // 2. Create Default Organization
  const org = await prisma.organization.upsert({
    where: { id: 'default-org-id' },
    update: {},
    create: {
      id: 'default-org-id',
      name: 'Основная организация',
      inn: '000000000',
      user_id: user.id,
      onboarding_state: 'COMPLETED',
    },
  })
  console.log(`Organization created: ${org.name}`)

  // 3. Seed MasterAccounts from shared utility
  const { defaultAccounts, seedDefaultDataForOrg } = await import('../src/lib/seed-utils');
  console.log('Seeding MasterAccounts...');
  for (const acc of defaultAccounts) {
    await prisma.masterAccount.upsert({
      where: { code: acc.code },
      update: { name: acc.name, type: acc.type },
      create: {
        code: acc.code,
        name: acc.name,
        type: acc.type,
        description: '',
        section: '',
        is_system: acc.code === '0000' || acc.code === '9910'
      }
    });
  }
  console.log(`MasterAccounts seeded: ${defaultAccounts.length}`)

  // 4. Update User's active_org_id
  await prisma.user.update({
    where: { id: user.id },
    data: { active_org_id: org.id }
  });
  console.log(`Updated user ${user.email} with active_org_id: ${org.id}`)

  // 5. Seed Accounts and Settings for the organization
  await seedDefaultDataForOrg(org.id);

  // 6. Seed Industry Templates
  console.log('Seeding IndustryTemplates...')
  for (const tmplData of INDUSTRY_TEMPLATES) {
    const tmpl = await prisma.industryTemplate.upsert({
      where: { key: tmplData.key },
      update: { name: tmplData.name, description: tmplData.description },
      create: {
        key: tmplData.key,
        name: tmplData.name,
        description: tmplData.description,
        sort_order: tmplData.sort_order,
        is_active: true,
      },
    })

    for (const code of tmplData.accounts) {
      const master = await prisma.masterAccount.findUnique({ where: { code } })
      if (!master) {
        console.warn(`  Счёт ${code} не найден в MasterAccount — пропускаем`)
        continue
      }
      await prisma.industryTemplateItem.upsert({
        where: {
          template_id_master_account_id: {
            template_id: tmpl.id,
            master_account_id: master.id,
          }
        },
        create: {
          template_id: tmpl.id,
          master_account_id: master.id,
          is_required: tmplData.required_accounts.includes(code),
        },
        update: {
          is_required: tmplData.required_accounts.includes(code),
        }
      })
    }
    console.log(`  Template "${tmplData.name}": ${tmplData.accounts.length} accounts`)
  }

  console.log('Seeding finished successfully.')
}

main()
  .catch((e) => {
    console.error(e)
    process.exit(1)
  })
  .finally(async () => {
    await prisma.$disconnect()
  })
